    private static string BuildPathKey(List<Cell> path)
    {
        string forward = BuildSequenceKey(path, false);
        string reverse = BuildSequenceKey(path, true);
        return string.CompareOrdinal(forward, reverse) <= 0 ? forward : reverse;
    }

    private static string BuildSequenceKey(List<Cell> path, bool reverse)
    {
        StringBuilder builder = new StringBuilder();
        if (reverse)
        {
            for (int i = path.Count - 1; i >= 0; i--)
            {
                AppendCellKey(builder, path[i]);
            }
        }
        else
        {
            for (int i = 0; i < path.Count; i++)
            {
                AppendCellKey(builder, path[i]);
            }
        }

        return builder.ToString();
    }

    private static void AppendCellKey(StringBuilder builder, Cell cell)
    {
        builder.Append(cell.X);
        builder.Append(',');
        builder.Append(cell.Y);
        builder.Append(';');
    }

    private static Cell FirstCell(HashSet<Cell> cells)
    {
        foreach (Cell cell in cells)
        {
            return cell;
        }

        return Cell.Zero;
    }

    private static bool IsInsideBoard(Cell cell, int boardWidth, int boardHeight)
    {
        return cell.X >= 0 && cell.X < boardWidth && cell.Y >= 0 && cell.Y < boardHeight;
    }

    private static int ManhattanDistance(Cell a, Cell b)
    {
        return Math.Abs(a.X - b.X) + Math.Abs(a.Y - b.Y);
    }

    private static int DistanceToBoardEdge(Cell cell, int boardWidth, int boardHeight)
    {
        int left = cell.X;
        int right = boardWidth - 1 - cell.X;
        int bottom = cell.Y;
        int top = boardHeight - 1 - cell.Y;
        return Math.Min(Math.Min(left, right), Math.Min(bottom, top));
    }

    private static int CeilDiv(int value, int divisor)
    {
        return divisor <= 0 ? 0 : (value + divisor - 1) / divisor;
    }

    private static int CompareCells(Cell a, Cell b)
    {
        int yCompare = a.Y.CompareTo(b.Y);
        return yCompare != 0 ? yCompare : a.X.CompareTo(b.X);
    }

    private static Cell DirectionToVector(ArrowDirection direction)
    {
        switch (direction)
        {
            case ArrowDirection.Up:
                return new Cell(0, 1);
            case ArrowDirection.Down:
                return new Cell(0, -1);
            case ArrowDirection.Left:
                return new Cell(-1, 0);
            case ArrowDirection.Right:
                return new Cell(1, 0);
            default:
                return Cell.Zero;
        }
    }

    private static void Shuffle<T>(List<T> list, System.Random random)
    {
        for (int i = list.Count - 1; i > 0; i--)
        {
            int swapIndex = random.Next(i + 1);
            T temp = list[i];
            list[i] = list[swapIndex];
            list[swapIndex] = temp;
        }
    }

    private static void LogFailure(GenerationContext context)
    {
        string reason = string.IsNullOrWhiteSpace(context.FailureReason)
            ? "unknown failure"
            : context.FailureReason;

        string timeLimit = context.MaxGenerationSeconds <= 0f
            ? "unlimited"
            : $"{context.MaxGenerationSeconds:0.##}s";
        string requestedTimeLimit = context.RequestedMaxGenerationSeconds <= 0f
            ? "unlimited"
            : $"{context.RequestedMaxGenerationSeconds:0.##}s";
        string searchLimit = context.MaxSearchFrames <= 0
            ? "unlimited"
            : context.MaxSearchFrames.ToString();

        Debug.LogWarning($"Path-cover level generation failed: {reason}. Tried arrow count range: {context.EffectiveMinArrowCount}-{context.EffectiveMaxArrowCount}. Search frames: {context.SearchFrames}. Greedy attempts: {context.GreedyAttempts}. Direction frames: {context.DirectionSearchFrames}. Candidate checks: {context.CandidateChecks}. Time limit: {timeLimit} requested: {requestedTimeLimit}. Search-frame limit: {searchLimit}. Elapsed: {context.Stopwatch.Elapsed.TotalSeconds:0.00}s.");
    }

