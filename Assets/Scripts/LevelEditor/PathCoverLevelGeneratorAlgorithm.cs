    private class DependencyGraph
    {
        private readonly Dictionary<int, HashSet<int>> edgesBySource = new Dictionary<int, HashSet<int>>();

        public int NodeCount => edgesBySource.Count;

        public void AddNode(int arrowId)
        {
            if (!edgesBySource.ContainsKey(arrowId))
            {
                edgesBySource.Add(arrowId, new HashSet<int>());
            }
        }

        public void AddEdge(int blockerArrowId, int blockedArrowId)
        {
            AddNode(blockerArrowId);
            AddNode(blockedArrowId);

            if (blockerArrowId == blockedArrowId)
            {
                return;
            }

            edgesBySource[blockerArrowId].Add(blockedArrowId);
        }

        public DependencyGraph Clone()
        {
            DependencyGraph clone = new DependencyGraph();
            foreach (KeyValuePair<int, HashSet<int>> entry in edgesBySource)
            {
                clone.AddNode(entry.Key);
                foreach (int blockedArrowId in entry.Value)
                {
                    clone.AddEdge(entry.Key, blockedArrowId);
                }
            }

            return clone;
        }

        public bool IsAcyclic()
        {
            return TopologicalSort().Count == NodeCount;
        }

        public List<int> TopologicalSort()
        {
            Dictionary<int, int> indegreeByArrowId = new Dictionary<int, int>();
            foreach (int arrowId in edgesBySource.Keys)
            {
                indegreeByArrowId[arrowId] = 0;
            }

            foreach (KeyValuePair<int, HashSet<int>> entry in edgesBySource)
            {
                foreach (int blockedArrowId in entry.Value)
                {
                    if (!indegreeByArrowId.ContainsKey(blockedArrowId))
                    {
                        indegreeByArrowId[blockedArrowId] = 0;
                    }

                    indegreeByArrowId[blockedArrowId]++;
                }
            }

            List<int> ready = new List<int>();
            foreach (KeyValuePair<int, int> entry in indegreeByArrowId)
            {
                if (entry.Value == 0)
                {
                    ready.Add(entry.Key);
                }
            }

            ready.Sort();
            List<int> result = new List<int>();
            while (ready.Count > 0)
            {
                int arrowId = ready[0];
                ready.RemoveAt(0);
                result.Add(arrowId);

                if (!edgesBySource.TryGetValue(arrowId, out HashSet<int> blockedArrows))
                {
                    continue;
                }

                List<int> sortedBlockedArrows = new List<int>(blockedArrows);
                sortedBlockedArrows.Sort();
                for (int i = 0; i < sortedBlockedArrows.Count; i++)
                {
                    int blockedArrowId = sortedBlockedArrows[i];
                    indegreeByArrowId[blockedArrowId]--;
                    if (indegreeByArrowId[blockedArrowId] == 0)
                    {
                        ready.Add(blockedArrowId);
                        ready.Sort();
                    }
                }
            }

            return result;
        }

        public List<DependencyData> ToDependencyData()
        {
            List<DependencyData> dependencies = new List<DependencyData>();
            foreach (KeyValuePair<int, HashSet<int>> entry in edgesBySource)
            {
                foreach (int blockedArrowId in entry.Value)
                {
                    dependencies.Add(new DependencyData(entry.Key, blockedArrowId));
                }
            }

            dependencies.Sort((a, b) =>
            {
                int blockerCompare = a.blockerArrowId.CompareTo(b.blockerArrowId);
                return blockerCompare != 0 ? blockerCompare : a.blockedArrowId.CompareTo(b.blockedArrowId);
            });
            return dependencies;
        }
    }

    private struct Cell : IEquatable<Cell>
    {
        public static readonly Cell Zero = new Cell(0, 0);

        public readonly int X;
        public readonly int Y;

        public Cell(int x, int y)
        {
            X = x;
            Y = y;
        }

        public bool Equals(Cell other)
        {
            return X == other.X && Y == other.Y;
        }

        public override bool Equals(object obj)
        {
            return obj is Cell other && Equals(other);
        }

        public override int GetHashCode()
        {
            unchecked
            {
                return (X * 397) ^ Y;
            }
        }

        public static bool operator ==(Cell left, Cell right)
        {
            return left.Equals(right);
        }

        public static bool operator !=(Cell left, Cell right)
        {
            return !left.Equals(right);
        }

        public static Cell operator +(Cell left, Cell right)
        {
            return new Cell(left.X + right.X, left.Y + right.Y);
        }

        public static Cell operator -(Cell left, Cell right)
        {
            return new Cell(left.X - right.X, left.Y - right.Y);
        }
    }
